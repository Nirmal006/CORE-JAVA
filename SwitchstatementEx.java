
public class SwitchstatementEx {

	public static void main(String[] args) {
		int a=3;
		 switch(a){
		//Case statements
		case 1: System.out.println("I am @ Home");
		break;
		case 2: System.out.println("I am @ Office");
		break;
		case 3: System.out.println("I am @ school");
		break;
		//Default case statement
		default:System.out.println("Not @ Home, Office, or school");
		}
		
		//char switch statement
		 char ch='z';
		 switch (ch) {
		 case 'a':System.out.println("say hello");
		 break;
		 case 'b':System.out.println("say hi");
		 break;
		 default:System.out.println("who is this");
		 }
		 

	}

}
